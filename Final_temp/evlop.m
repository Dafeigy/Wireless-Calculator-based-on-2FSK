function [pyr1,f3,f2,f1]=evlop(bp2000,bp1000,lpf,aaaa,pyr)
f10=filter(bp2000,1,pyr);%仅选取2000HZ的频率，代表0
f20=filter(bp1000,1,pyr);
%全波整流后进行包洛检波
f1=abs(f10);
f2=abs(f20);
subplot(3,2,1);
plot(f1);
subplot(3,2,2);
plot(f2);
f1=filter(lpf,1,f1);
f2=filter(lpf,1,f2);
%抽样判决
subplot(3,2,3);
plot(f1);
subplot(3,2,4);
plot(f2);
pyr1=abs(f2)-abs(f1);
f3=downsample(pyr1,aaaa);
subplot(3,2,5);
plot(f3);
%下采样
subplot(3,2,6);
plot(f3);
end 